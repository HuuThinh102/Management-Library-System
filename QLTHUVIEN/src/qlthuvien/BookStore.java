    public void inKhoSach(){
        int c;
        try {
            pst = conn.prepareStatement("select * from qlthuvien.khosach");
            resultSet = pst.executeQuery();
            ResultSetMetaData rsd = (ResultSetMetaData) resultSet.getMetaData();
            c = rsd.getColumnCount();
            DefaultTableModel d = (DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            while(resultSet.next()){
                Vector v2 = new Vector();
                for(int i = 1; i <= c; i++){
                    v2.add(resultSet.getString("IDS"));
                    v2.add(resultSet.getString("tenSach"));
                    v2.add(resultSet.getString("tacGia"));
                    v2.add(resultSet.getString("soLuong"));
                }
                d.addRow(v2);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(BookStore.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    public void inKhoSach(String tensach) {
        int c;
        try {
            pst = conn.prepareStatement("select * from qlthuvien.khosach where tenSach = ?");
            pst.setString(1,tensach);
            resultSet = pst.executeQuery();
            ResultSetMetaData rsd = (ResultSetMetaData) resultSet.getMetaData();
            c = rsd.getColumnCount();
            DefaultTableModel d = (DefaultTableModel) jTable1.getModel();
            d.setRowCount(0);
            while (resultSet.next()) {
                Vector v2 = new Vector();
                for (int i = 1; i <= c; i++) {
                    v2.add(resultSet.getString("IDS"));
                    v2.add(resultSet.getString("tenSach"));
                    v2.add(resultSet.getString("tacGia"));
                    v2.add(resultSet.getString("soLuong"));
                }
                d.addRow(v2);
            }

        } catch (SQLException ex) {
            Logger.getLogger(BookStore.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
