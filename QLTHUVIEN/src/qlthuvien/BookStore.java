    private void themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_themActionPerformed
        String ID = id.getText();
        String bookName = tensach.getText();
        String author = tacgia.getText();
        String numBook = soluong.getText();
        if(ID.equals("")||bookName.equals("")||author.equals("")||numBook.equals("")){
            JOptionPane.showConfirmDialog(rootPane, "Vui lòng nhập đầy đủ thông tin!", "Error", 2);
        }else{
            try {
                pst = conn.prepareStatement("CALL THEM_BOOK(?,?,?,?)");
                pst.setString(1,ID);
                pst.setString(2,bookName);
                pst.setString(3,author);
                pst.setString(4,numBook);
                int k = pst.executeUpdate();
                if(k==1){
                    JOptionPane.showMessageDialog(null, "Thêm sách thành công");
                    id.setText("");
                    tensach.setText("");
                    tacgia.setText("");
                    soluong.setText("");
                    inKhoSach();
                }else{
                    JOptionPane.showMessageDialog(null, "Thêm sách thất bại");
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "ID sách bị trùng, vui lòng nhập ID khác!!!"); 
            }
        }
                
    }//GEN-LAST:event_themActionPerformed

