    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel d1 = (DefaultTableModel) jTable1.getModel();
        int selectIndex = jTable1.getSelectedRow();
        String id = jTextFieldIDs.getText();
        String tensv = jTextFieldTSV.getText();
        String tenSach = jTextFieldTST.getText();
        if (id.equals("") || tensv.equals("") || tenSach.equals("")) {
            JOptionPane.showConfirmDialog(rootPane, "Vui lòng nhập đầy đủ thông tin!", "Error", 2);
        } else {
            try {
                pst = conn.prepareStatement("select * from qlthuvien.users where ID = ?");
                pst.setString(1, id);
                resultSet = pst.executeQuery();
                if (resultSet.next()) {
                    pst = conn.prepareStatement("select IDS from qlthuvien.khosach where tenSach = ?");
                    pst.setString(1, tenSach);
                    resultSet = pst.executeQuery();
                    if (resultSet.next()) {
                        String ids = resultSet.getString(1);
                        pst = conn.prepareStatement("delete from qlthuvien.muonsach where ID=? and IDS=?");
                        pst.setString(1, id);
                        pst.setString(2, ids);
                        int k = pst.executeUpdate();
                        if (k == 1) {
                            pst = conn.prepareStatement("select soLuong from qlthuvien.khosach where IDS = ?");
                            pst.setString(1, ids);
                            resultSet = pst.executeQuery();
                            if (resultSet.next()) {                              
                                int numbook = resultSet.getInt(1);                              
                                JOptionPane.showMessageDialog(null, "Trả sách thành công");
                                pst = conn.prepareStatement("update qlthuvien.khosach set soLuong = ? where IDS = ?");
                                pst.setInt(1, numbook+1);
                                pst.setString(2, ids);
                                pst.executeUpdate();
                                jTextFieldIDs.setText("");
                                jTextFieldTSV.setText("");
                                jTextFieldTST.setText("");
                                inKhoSach();
                            }else{
                                JOptionPane.showMessageDialog(null, "Cập nhật thất bại");
                            }

                        } else {
                            JOptionPane.showMessageDialog(null, "Cập nhật thất bại");
                        }
                    }

                }

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Cập nhật thất bại?");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

