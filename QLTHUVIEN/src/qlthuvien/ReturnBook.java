    public void inKhoSach() {
        int c;
        try {
            pst = conn.prepareStatement("select u.ID, u.name, k.tenSach, m.date, m.dateReturn from khosach k, muonsach m, users u where k.IDS = m.IDS and u.ID = m.ID;");
            resultSet = pst.executeQuery();
            ResultSetMetaData rsd = (ResultSetMetaData) resultSet.getMetaData();
            c = rsd.getColumnCount();
            DefaultTableModel d = (DefaultTableModel) jTable1.getModel();
            d.setRowCount(0);
            while (resultSet.next()) {
                Vector v2 = new Vector();
                for (int i = 1; i <= c; i++) {
                    v2.add(resultSet.getString("ID"));
                    v2.add(resultSet.getString("name"));
                    v2.add(resultSet.getString("tenSach"));
                    v2.add(resultSet.getString("date"));
                    v2.add(resultSet.getString("dateReturn"));
                }
                d.addRow(v2);
            }

        } catch (SQLException ex) {
            Logger.getLogger(BookStore.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
    
